from django import forms
from .models import Submission


# =========================================================
# טופס StudentSubmissionForm
# =========================================================
# זה הטופס שהתלמידים ימלאו באתר.
#
# הטופס מבוסס על המודל Submission,
# ולכן הוא נקרא ModelForm.
#
# היתרון:
# במקום לבנות טופס ידנית ולשמור כל שדה לבד,
# Django יודע:
# - להציג את השדות
# - לבדוק תקינות בסיסית
# - ולשמור את הנתונים למסד הנתונים
# =========================================================
class StudentSubmissionForm(forms.ModelForm):

    # -----------------------------------------------------
    # Meta
    # -----------------------------------------------------
    # המחלקה הפנימית Meta אומרת ל-Django:
    # 1. על איזה מודל הטופס מבוסס
    # 2. אילו שדות מתוך המודל יופיעו בטופס
    # -----------------------------------------------------
    class Meta:
        model = Submission

        # -------------------------------------------------
        # fields
        # -------------------------------------------------
        # כאן אנחנו בוחרים אילו שדות להציג לתלמיד.
        #
        # שים לב:
        # אנחנו לא כוללים כאן את status או approved_at,
        # כי תלמידים לא אמורים לשלוט בזה.
        #
        # status יישאר אוטומטית "pending"
        # עד שמבוגר יאשר דרך ה-admin.
        # -------------------------------------------------
        fields = [
            "title",
            "student_name",
            "class_name",
            "media_type",
            "media_file",
            "parent_approval",
        ]

        # -------------------------------------------------
        # widgets
        # -------------------------------------------------
        # widgets מאפשרים לשלוט באיך כל שדה ייראה בטופס HTML.
        #
        # כאן אנחנו מוסיפים:
        # - placeholder לשדות טקסט
        # - checkbox ברור לשדה אישור הורים
        # -------------------------------------------------
        widgets = {
            "title": forms.TextInput(attrs={
                "placeholder": "למשל: משימת שלום מהבית",
                "class": "form-control",
            }),
            "student_name": forms.TextInput(attrs={
                "placeholder": "שם פרטי או כינוי",
                "class": "form-control",
            }),
            "class_name": forms.TextInput(attrs={
                "placeholder": "למשל: ז1 או 7A",
                "class": "form-control",
            }),
            "media_type": forms.Select(attrs={
                "class": "form-control",
            }),
            "media_file": forms.ClearableFileInput(attrs={
                "class": "form-control",
            }),
            "parent_approval": forms.CheckboxInput(attrs={
                "class": "form-check-input",
            }),
        }

        # -------------------------------------------------
        # labels
        # -------------------------------------------------
        # labels הם השמות שיופיעו ליד השדות בטופס.
        # כך הממשק יהיה בעברית וברור יותר.
        # -------------------------------------------------
        labels = {
            "title": "כותרת",
            "student_name": "שם פרטי או כינוי",
            "class_name": "כיתה",
            "media_type": "סוג הקובץ",
            "media_file": "העלאת קובץ",
            "parent_approval": "אני מאשר/ת שיש אישור הורים לפרסום",
        }

        # -------------------------------------------------
        # help_texts
        # -------------------------------------------------
        # טקסט עזרה קטן שמופיע ליד השדות.
        # זה עוזר למשתמש להבין מה למלא.
        # -------------------------------------------------
        help_texts = {
            "student_name": "לא למלא שם מלא.",
            "media_file": "אפשר להעלות סרטון או תמונה בלבד.",
        }

    # =====================================================
    # clean_media_file
    # =====================================================
    # פונקציית validation מותאמת אישית לשדה media_file.
    #
    # Django קורא לפונקציה הזו אוטומטית כאשר שם הפונקציה הוא:
    # clean_<field_name>
    #
    # כאן אנחנו בודקים:
    # - שבאמת הועלה קובץ
    # - שהקובץ לא גדול מדי
    # - שהסיומת מותרת
    # =====================================================
    def clean_media_file(self):
        media_file = self.cleaned_data.get("media_file")

        # אם לא הועלה קובץ, נחזיר שגיאה
        if not media_file:
            raise forms.ValidationError("חובה להעלות קובץ.")

        # -------------------------------------------------
        # בדיקת גודל קובץ
        # -------------------------------------------------
        # כאן הגבלנו את גודל הקובץ ל-30MB.
        # אפשר לשנות בעתיד אם תרצה.
        # -------------------------------------------------
        max_size = 30 * 1024 * 1024  # 30MB

        if media_file.size > max_size:
            raise forms.ValidationError("הקובץ גדול מדי. אפשר להעלות עד 30MB.")

        # -------------------------------------------------
        # בדיקת סיומת קובץ
        # -------------------------------------------------
        # אנחנו בודקים את שם הקובץ כדי לוודא
        # שהוא מסתיים בסיומת מותרת.
        #
        # זו לא הגנה מושלמת, אבל ל-MVP זו התחלה טובה.
        # -------------------------------------------------
        allowed_extensions = [".mp4", ".mov", ".jpg", ".jpeg", ".png"]
        filename = media_file.name.lower()

        if not any(filename.endswith(ext) for ext in allowed_extensions):
            raise forms.ValidationError(
                "סוג קובץ לא נתמך. אפשר להעלות רק MP4, MOV, JPG, JPEG, PNG."
            )

        return media_file

    # =====================================================
    # clean_student_name
    # =====================================================
    # בדיקה נוספת כדי לעזור לשמור על פרטיות.
    #
    # הרעיון:
    # אם תלמיד מזין שם ארוך מדי, נבקש ממנו לקצר.
    # זה לא מוכיח ב-100% שזה לא שם מלא,
    # אבל זה כן עוזר לכוון לשם פרטי או כינוי.
    # =====================================================
    def clean_student_name(self):
        student_name = self.cleaned_data.get("student_name", "").strip()

        if len(student_name) > 30:
            raise forms.ValidationError("נא למלא שם פרטי או כינוי קצר בלבד.")

        return student_name
    