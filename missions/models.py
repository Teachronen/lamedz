from django.db import models


# =========================================================
# מודל Submission
# =========================================================
# המודל הזה מייצג העלאה אחת של תלמיד.
# כל פעם שתלמיד מעלה סרטון או תמונה,
# נוצרת רשומה אחת בטבלה הזו במסד הנתונים.
#
# הרשומה מכילה מידע על:
# - שם התלמיד
# - הכיתה
# - הקובץ שהועלה
# - האם יש אישור הורים
# - האם המנהל אישר לפרסום
# - מתי ההעלאה נוצרה
#
# Django ישתמש במודל הזה כדי ליצור טבלה במסד הנתונים.
# =========================================================

class Submission(models.Model):

    # -----------------------------------------------------
    # STATUS_CHOICES
    # -----------------------------------------------------
    # רשימת האפשרויות לסטטוס של ההעלאה.
    #
    # Django משתמש בזה כדי ליצור:
    # - תפריט בחירה בממשק הניהול
    # - ולוודא שהערכים במסד הנתונים תקינים.
    #
    # pending  = הסרטון הועלה אבל עדיין לא אושר
    # approved = הסרטון אושר ויכול להופיע באתר
    # rejected = הסרטון נדחה ולא יוצג
    # -----------------------------------------------------

    STATUS_CHOICES = [
        ("pending", "Pending"),
        ("approved", "Approved"),
        ("rejected", "Rejected"),
    ]


    # -----------------------------------------------------
    # MEDIA_TYPE
    # -----------------------------------------------------
    # סוג הקובץ שהועלה.
    #
    # כרגע אנחנו מאפשרים:
    # - וידאו
    # - תמונה
    #
    # זה יעזור לנו בעתיד להחליט איך להציג את הקובץ
    # בגלריה (video tag או img tag).
    # -----------------------------------------------------

    MEDIA_TYPE = [
        ("video", "Video"),
        ("image", "Image"),
    ]


    # -----------------------------------------------------
    # title
    # -----------------------------------------------------
    # כותרת הסרטון / התמונה.
    #
    # לדוגמה:
    # "משימת שלום מהבית"
    # -----------------------------------------------------

    title = models.CharField(max_length=200)


    # -----------------------------------------------------
    # student_name
    # -----------------------------------------------------
    # שם פרטי או כינוי של התלמיד.
    #
    # לא נשתמש בשם מלא כדי לשמור על פרטיות.
    # -----------------------------------------------------

    student_name = models.CharField(
        max_length=100,
        help_text="First name or nickname only"
    )


    # -----------------------------------------------------
    # class_name
    # -----------------------------------------------------
    # שם הכיתה.
    #
    # לדוגמה:
    # 7A
    # 8B
    # -----------------------------------------------------

    class_name = models.CharField(
        max_length=50,
        help_text="Example: 7A"
    )


    # -----------------------------------------------------
    # media_type
    # -----------------------------------------------------
    # סוג המדיה.
    #
    # משתמש ב MEDIA_TYPE כדי להבטיח
    # שהערך יהיה רק video או image.
    # -----------------------------------------------------

    media_type = models.CharField(
        max_length=10,
        choices=MEDIA_TYPE
    )


    # -----------------------------------------------------
    # media_file
    # -----------------------------------------------------
    # הקובץ עצמו שהתלמיד העלה.
    #
    # FileField גורם ל-Django:
    # - לשמור את הקובץ בתיקיית MEDIA_ROOT
    # - לשמור במסד הנתונים את הנתיב לקובץ
    #
    # upload_to אומר לשמור בתוך:
    #
    # media/submissions/
    # -----------------------------------------------------

    media_file = models.FileField(
        upload_to="submissions/"
    )


    # -----------------------------------------------------
    # parent_approval
    # -----------------------------------------------------
    # האם התלמיד סימן שיש אישור מההורים לפרסום.
    #
    # ברירת מחדל: False
    # -----------------------------------------------------

    parent_approval = models.BooleanField(
        default=False,
        help_text="Parent permission confirmed"
    )


    # -----------------------------------------------------
    # status
    # -----------------------------------------------------
    # הסטטוס של ההעלאה.
    #
    # ברירת המחדל היא pending.
    #
    # רק כאשר status = approved
    # הסרטון יוצג באתר הציבורי.
    # -----------------------------------------------------

    status = models.CharField(
        max_length=10,
        choices=STATUS_CHOICES,
        default="pending"
    )


    # -----------------------------------------------------
    # created_at
    # -----------------------------------------------------
    # הזמן שבו ההעלאה נוצרה.
    #
    # auto_now_add אומר ל-Django
    # למלא את הזמן אוטומטית בעת יצירת הרשומה.
    # -----------------------------------------------------

    created_at = models.DateTimeField(
        auto_now_add=True
    )


    # -----------------------------------------------------
    # approved_at
    # -----------------------------------------------------
    # הזמן שבו ההעלאה אושרה.
    #
    # השדה הזה יכול להיות ריק אם
    # הסרטון עדיין לא אושר.
    # -----------------------------------------------------

    approved_at = models.DateTimeField(
        null=True,
        blank=True
    )


    # -----------------------------------------------------
    # __str__
    # -----------------------------------------------------
    # הפונקציה הזו קובעת איך האובייקט יוצג
    # בממשק הניהול של Django.
    #
    # במקום לראות:
    # Submission object (1)
    #
    # נראה משהו כמו:
    # "שלום מהבית - יואב"
    # -----------------------------------------------------

    def __str__(self):
        return f"{self.title} - {self.student_name}"
    